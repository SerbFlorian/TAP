package functional;

/**
 * Enumeración que representa los géneros posibles para una persona.
 *
 * <p>Esta enumeración define los valores:</p>
 * <ul>
 *   <li>MALE - para representar el género masculino.</li>
 *   <li>FEMALE - para representar el género femenino.</li>
 * </ul>
 *
 * <p>Esta estructura permite asignar y utilizar géneros de forma consistente y segura.</p>
 *
 * @author MikeW
 */
public enum Gender {
    MALE,   // Masculino
    FEMALE  // Femenino
}
