package solutions.accounts;

/**
 * Enumeración `AccType` que representa los diferentes tipos de cuentas bancarias.
 * Cada tipo de cuenta se identifica mediante un valor específico.
 */
public enum AccType {

    /**
     * Cuenta de inversión (Investment Fund)
     */
    IF,

    /**
     * Cuenta corriente (Current Account)
     */
    CA,

    /**
     * Cuenta bancaria estándar (Bank Account)
     */
    BA,

    /**
     * Cuenta de ahorros (Savings Account)
     */
    SA
}
